export class Car {
  constructor(id, make, model, year, vin, color, price, mileage, car_type) {
    this.id = id;
    this.make = make;
    this.model = model;
    this.year = year;
    this.vin = vin;
    this.color = color;
    this.price = price;
    this.mileage = mileage;
    this.car_type = car_type;
  }
}